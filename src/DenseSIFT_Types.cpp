namespace dc
{
	bool FeatureType::operator == (const FeatureType& f) const
	{
		return (scale == f.scale) && (orient == f.orient) &&
			(x == f.x) && (y == f.y);
	}

	std::istream& operator>>(std::istream& is, FeatureType& feature)
	{
		is >> feature.x >> feature.y >> feature.scale >> feature.orient;
		return is;
	}

}	// namespace dc